let rec eval_l (exp : expr) (env : Env.env) : Env.value =
  match exp with
  | Var v -> Env.lookup env v
  | Num x -> Env.Val (Num x)
  | Bool b -> Env.Val (Bool b)
  | Unop (_, e) -> 
